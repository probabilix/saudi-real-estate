// Get featured listings
export function getFeaturedListings(): ListingWithOwner[] {
  return mockListings.filter((l) => l.isFeatured);
}

// Get listings by city
export function getListingsByCity(city: string): ListingWithOwner[] {
  return mockListings.filter((l) => l.city === city);
}

// Get a single listing
export function getListingById(id: string): ListingWithOwner | undefined {
  return mockListings.find((l) => l.id === id);
}

// Filter listings
export function filterListings(filters: {
  city?: string;
  type?: string;
  purpose?: string;
  minPrice?: number;
  maxPrice?: number;
  bedrooms?: number;
}): ListingWithOwner[] {
  return mockListings.filter((l) => {
    if (filters.city && l.city !== filters.city) return false;
    if (filters.type && l.type !== filters.type) return false;
    if (filters.purpose && l.purpose !== filters.purpose) return false;
    if (filters.minPrice && l.price < filters.minPrice) return false;
    if (filters.maxPrice && l.price > filters.maxPrice) return false;
    if (filters.bedrooms && l.bedrooms !== filters.bedrooms) return false;
    return true;
  });
}
